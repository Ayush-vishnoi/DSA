public class alpha {
    public static void main(String[] args){
        int n=5;
        char ch='A';
        for(int i=1;i<=n;i++){
            
            for(char j='A';j<=ch+i-1;j++){
               
                System.out.print(j+" ");
               
        }
        System.out.println(" ");
    }
}
}
