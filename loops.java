public class loops {
    public static void main(String[] args){
        //for loop
        System.out.println("Table of 3:");
        for(int i=1;i<=10;i++){
            System.out.println("3 * "+i+" = "+3*i);
        }
        //while loop
        System.out.println("\nTable of 4:");
        int j=1;
        while(j<=10){
            System.out.println("4 * "+j+" = "+4*j);
            j++;
        }
    } 
}
